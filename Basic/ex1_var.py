# 整數與浮點數
num1 = 34 # int
num2 = 67.83 # float
num3 = 34.0 # float
print("num1:", num1, "; type:", type(num1))
print("num2:", num2, "; type:", type(num2))
print("num3:", num3, "; type:", type(num3))
print("-" * 50 + "\n")

# bool
flag = True
print("flag:", flag, "; type:", type(flag))
print("-" * 50 + "\n")

# str
s1 = "str1"
print("s1:", s1, "; type:", type(s1))
print("-" * 50 + "\n")
